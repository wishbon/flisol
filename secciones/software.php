		<h1>¿Qué es Software Libre?</h1>
		<div class="izqder">
			<div class="texto">
				<h2>Software libre es</h2>
				<p>El software que respeta la libertad de los usuarios y la comunidad. A grandes rasgos, significa que los usuarios tienen la libertad de ejecutar, copiar, distribuir, estudiar, modificar y mejorar el software. Es decir, el «software libre» es una cuestión de libertad, no de precio.</p><br />
				<p>Un programa informático es software libre si otorga a los usuarios todas estas libertades de manera adecuada. De lo contrario no es libre. Existen diversos esquemas de distribución que no son libres, y si bien podemos distinguirlos sobre la base de cuánto les falta para llegar a ser libres, su uso bien puede ser considerado contrario a la ética en todos los casos por igual.</p>
				<p><br />
					Un programa es software libre si los usuarios tienen las cuatro libertades esenciales:</p><br />
					<ul>
						<li>

							La libertad de ejecutar el programa como se desea, con cualquier propósito (libertad 0).</li>
							<li>La libertad de estudiar cómo funciona el programa, y cambiarlo para que haga lo que usted quiera (libertad 1). El acceso al código fuente es una condición necesaria para ello.</li>
							<li>La libertad de redistribuir copias para ayudar a su prójimo (libertad 2).</li>
							<li>La libertad de distribuir copias de sus versiones modificadas a terceros (libertad 3). Esto le permite ofrecer a toda la comunidad la oportunidad de beneficiarse de las modificaciones. El acceso al código fuente es una condición necesaria para ello.</li>
						</ul>

