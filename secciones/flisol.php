			<div class="texto"><h2>Nos motiva</h2>
				<p>Dar a conocer el Software Libre de mano de su representante más conocido, GNU con linux, o por medio de la instalación de Software Libre como LibreOffice, GIMP, Firefox y muchos más. Durante el evento se instala de manera gratuita el sistema operativo y las alternativas libres a todas las personas que deseen y puedan llevar sus equipos a las sedes designadas por la directiva del festival.</p><br />

				<p>Durante el mismo se ofrecen de manera paralela charlas, ponencias y talleres, sobre temas locales, nacionales y latinoamericanos, relacionadas al Software Libre y al movimiento del mismo nombre.</p><br />

				<p>Es importante mencionar, que el objetivo es crear lazos y promover la migración de los usuarios, tanto principiantes como avanzados, al uso del software libre.</p><br />

				<p>El evento sirve también para brindar información sobre soluciones de bajo costo pero bastante poderosas a pequeñas y medianas empresas, que van desde un sistema operativo y un software ofimático, pasando por servidores web hasta llegar a implementación de plantas telefónicas.</p>
