				<h2>Flisol es</h2>
				<p>El Festival Latinoamericano de Instalación de Software Libre. Es el mayor evento de difusión del Software libre que se realiza desde el año 2005 en diferentes países de manera simultánea. En el año 2013 participaron más de 260 ciudades en 20 países de Latinoamérica.</p><br />
				<p>Es una oportunidad para todas aquellas personas interesadas en conocer más acerca del software libre. Participando es posible entrar en contacto con el mundo del software libre, conocer a otros usuarios, resolver dudas e interrogantes, intercambiar opiniones y experiencias; asistir a charlas y otras actividades programadas.</p><br />
				<p>Es de resaltar que la asistencia al evento es totalmente libre y gratuita.</p><br>
